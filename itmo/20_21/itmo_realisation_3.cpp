#include <iostream>
#include <iomanip>
#include <vector>
#include <map>
#include <set>

using namespace std;

#define int long long
int n = 10;
int m = 20;

signed main() {
    cin.tie(nullptr);
    ios_base::sync_with_stdio(false);
    int t;
    cin >> t;

    while (t--) {
        string first_line;
        bool flag = true;
        int cnt = 0;
        for (int i = 0; i < n; ++i) {
            string row;
            cin >> row;

            if (i == 0) {
                first_line = row;
                for (int j = 0; j < m; ++j) {
                    if (row[j] == '#') {
                        while (j < m && row[j] == '#') {
                            ++j;
                        }
                        cnt += 1;
                    }
                }
            } else {
                for (int j = 0; j < m; ++j) {
                    if (row[j] != first_line[j]) {
                        flag = false;
                    }
                }
            }
        }

        if (!flag || (cnt != 1 && cnt != 3)) {
            cout << "Incorrect\n";
        } else {
            if (cnt == 1) {
                cout << "Negative\n";
            } else {
                cout << "Positive\n";
            }
        }
    }


    return 0;
}

